package com.company;

public class Main {

    public static void main(String[] args) {
        int myVariable = 50; //the complete line is a statement
        if (myVariable == 50) {
            System.out.println("Indented");
        }

        myVariable++; //a statement
        System.out.println("This is a test"); //the complete line is a statement
        System.out.println("This is" +
                " another" +
                " still more.");
        //this is a statement
        //white space is the space between  expressions, operators and so on
    }
}
